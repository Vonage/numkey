    @Test
    @Parameters(method = "testNumKey_Parameters")
    public void numkeyString(String country, String number, long nk, String ns) throws Throwable {
        long h = NumKey.numkey(country, number);
        assertEquals(nk, h);
    }

    @Test
    @Parameters(method = "testNumKey_Parameters")
    public void numkeyByte(String country, String number, long nk, String ns) throws Throwable {
        long h = NumKey.numkey(country.getBytes(), number.getBytes());
        assertEquals(nk, h);
    }

    @Test
    @Parameters(method = "testNumKey_Parameters")
    public void decodeNumkeyStr(String country, String number, long nk, String ns) throws Throwable {
        NumKey.NumDataStr h = NumKey.decodeNumkeyStr(nk);
        assertEquals(country, h.country);
        assertEquals(number, h.number);
    }

    @Test
    @Parameters(method = "testNumKey_Parameters")
    public void decodeNumkey(String country, String number, long nk, String ns) throws Throwable {
        NumKey.NumData h = NumKey.decodeNumkey(nk);
        assertArrayEquals(country.getBytes(), h.country);
        assertArrayEquals(number.getBytes(), h.number);
    }

