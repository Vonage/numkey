class TestFunctions(TestCase):

    def test_numkey(self):
        for country, number, nk, _ in numkeyTestData:
            h = numkey.numkey(country, number)
            self.assertEqual(h, nk)

    def test_numkey_long(self):
        h = numkey.numkey(b"XX", b"9876543210987654321")
        self.assertEqual(h, 0xc61ee0c29f50cb10)

    def test_decode_numkey(self):
        for country, number, nk, _ in numkeyTestData:
            h = numkey.decode_numkey(nk)
            self.assertEqual(h[0], country)
            self.assertEqual(h[1], number)

    def test_decode_numkey_long(self):
        h = numkey.decode_numkey(0xc61ee0c29f50cb10)
        self.assertEqual(h[0], b"XX")
        self.assertEqual(h[1], b"")

    def test_compare_numkey_country(self):
        vkcmpTestData = [
            (0xd59d91591390125f, 0xd66a97d4e91646df, -1),
            (0xd6a23089b8e15cdf, 0xd6a2300000000000, 0),
            (0xd59d91591390125f, 0xd5347bf4170a666f, 1),
        ]
        for nka, nkb, exp in vkcmpTestData:
            res = numkey.compare_numkey_country(nka, nkb)
            self.assertEqual(res, exp)

    def test_numkey_hex(self):
        for _, _, nk, ns in numkeyTestData:
            h = numkey.numkey_hex(nk)
            self.assertEqual(h, ns)

    def test_parse_numkey_hex(self):
        for _, _, nk, ns in numkeyTestData:
            h = numkey.parse_numkey_hex(ns)
            self.assertEqual(h, nk)

    def test_parse_numkey_hex_input_type(self):
        self.assertEqual(numkey.parse_numkey_hex(b"d6a23089b8e15cdf"), numkey.parse_numkey_hex("d6a23089b8e15cdf"))


